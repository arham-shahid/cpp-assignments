#include <iostream>
using namespace std;

int main()
{
    int num_1 = 20,
        num_2 = 4,
        num_3 = 5;

    cout << "ADDITION = " << (num_1 + num_2 + num_3) << endl;
    cout << "SUBTRACTION = " << (num_1 - num_2 - num_3) << endl;
    cout << "MULTIPLICATION = " << (num_1 * num_2 * num_3) << endl;
    cout << "DIVISION = " << (num_1 / num_2 / num_3) << endl;
    cout << "MODULUS = " << (num_1 % num_2 % num_3) << endl;

    return 0;
}